import { getInitialSettings } from '@anthropic/config'
import { t } from '../i18n/index.js'

export function getSpinnerVerbs(): string[] {
  const settings = getInitialSettings()
  const config = settings.spinnerVerbs
  if (!config) {
    return getSpinnerVerbsBase()
  }
  if (config.mode === 'replace') {
    return config.verbs.length > 0 ? config.verbs : getSpinnerVerbsBase()
  }
  return [...getSpinnerVerbsBase(), ...config.verbs]
}

