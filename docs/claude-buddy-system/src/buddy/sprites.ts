/** 帽子 → ASCII 字符串 (替换精灵第一行空行) */
export const HAT_SPRITES: Record<Hat, string> = {
  none: '',
  crown: '   \\^^^/    ',
  tophat: '   [___]    ',
  propeller: '    -+-     ',
  halo: '   (   )    ',
  wizard: '    /^\\     ',
  beanie: '   (___)    ',
  tinyduck: '    ,>      ',
};

/** 动画帧率 (ms) */
export const FRAME_INTERVAL = 500;

// ─── 渲染函数 ─────────────────────────────────────────────

/**
 * 渲染一帧精灵
 *
 * 源码中的变量映射: Ud8 = renderSprite
 *
 * @param companion - 需要 species, eye, hat 字段
 * @param frameIndex - 动画帧索引 (自动取模)
 * @returns 渲染后的行数组
 */
export function renderSprite(
  companion: { species: Species; eye: Eye; hat: Hat },
  frameIndex: number = 0,
): string[] {
  const frames = SPRITE_FRAMES[companion.species];
  const frame = frames[frameIndex % frames.length];

  // 替换 {E} 占位符为实际眼睛字符
  const lines = [...frame.map((line) => line.replaceAll('{E}', companion.eye))];

  // 如果有帽子且第一行为空白，替换为帽子
  if (companion.hat !== 'none' && !lines[0].trim()) {
    lines[0] = HAT_SPRITES[companion.hat];
  }

  // 如果第一行仍为空白，且所有帧的第一行都为空白，删除它
  if (!lines[0].trim() && frames.every((f) => !f[0].trim())) {
    lines.shift();
  }

  return lines;
}

/**
 * 获取物种的总帧数
 *
 * 源码中的变量映射: SFK = getFrameCount
 */
export function getFrameCount(species: Species): number {
  return SPRITE_FRAMES[species].length;
}

/**
 * 获取窄终端下的表情文字脸 (fallback)
 *
 * 源码中的变量映射: CFK = getEmojiFace
 */
export function getEmojiFace(companion: { species: Species; eye: Eye }): string {
  const e = companion.eye;
  switch (companion.species) {
    case 'duck':
    case 'goose':
      return `(${e}>`;
    case 'cat':
      return `=${e}ω${e}=`;
    case 'owl':
      return `(${e})(${e})`;
    case 'rabbit':
      return `(${e}..${e})`;
    case 'mushroom':
      return `|${e}  ${e}|`;
    case 'chonk':
      return `(${e}.${e})`;
    default:
      return `(${e} ${e})`;
  }
}
